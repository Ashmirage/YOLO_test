import cv2

from ultralytics import YOLO

model = YOLO(r"yolo11n.pt")

results = model(
    source=0,
    stream=True,
    line_width=20,
)

for result in results:
    plotted = result.plot()
    cv2.imshow("YOLO Inference", plotted)
    if cv2.waitKey(1) & 0xFF == ord("q"):
        break

cv2.destroyAllWindows()
